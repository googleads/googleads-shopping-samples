# Handles common tasks across all MCA samples.

require_relative '../shopping_common'

# Creates an example account with the provided name.
def create_example_account(name)
  return Google::Apis::ContentV2::Account.new({
      :name => name,
      :website_url => "https://#{name}.example.com"})
end
